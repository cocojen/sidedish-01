import UIKit

final class LoginButton: UIButton {
    override init(frame: CGRect) {
        super.init(frame: frame)
        configureLayer()
    }

    required init?(coder: NSCoder) {
        super.init(coder: coder)
        configureLayer()
    }
    
    private func configureLayer() {
        layer.cornerRadius = 10
    }
}
